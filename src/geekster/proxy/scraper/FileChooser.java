    private final String text = CheckProxies.getProxyOutput();

    public FileChooser() {
        initComponents();
        setupFileChooserListener();
        String protocol = CheckProxies.getProtocolType().toLowerCase();
        fileChooser.setSelectedFile(new File(protocol + ".txt"));
    }

    private void setupFileChooserListener() {
        fileChooser.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent evt) {
                if (JFileChooser.APPROVE_SELECTION.equals(evt.getActionCommand())) {
                    saveFile();
                }
                else if(JFileChooser.CANCEL_SELECTION.equals(evt.getActionCommand())) {
                    dispose();
                }
            }
        });
    }

    public void saveFile() {
        
        File path = fileChooser.getSelectedFile();
        try (FileWriter writer = new FileWriter(path)) {
            writer.write(text);
            JOptionPane.showMessageDialog(FileChooser.this, "File saved to: " + path);
        } 
        catch (IOException ioException) {
            ioException.printStackTrace();
            JOptionPane.showMessageDialog(FileChooser.this, "Error saving file.");
        }
    }
    
